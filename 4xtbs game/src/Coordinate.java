
public class Coordinate
{
	//Constructor instance variables
	private int x;
	private int y;
	
	//Coordinate constructor
	public Coordinate(int X, int Y)
	{
		x = X;
		y = Y;
	}

	//Getters and setters
	public int getX()
	{
		return x;
	}
	public int getY() 
	{
		return y;
	}
}